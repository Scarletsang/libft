#include <string.h>

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	char	*dest_start;

	dest_start = dest;
	while (*dest)
	{
		dest++;
	}
	while (*src && n > 0)
	{
		*dest = *src;
		dest++;
		src++;
		n--;
	}
	*dest = 0;
	return (dest_start);
}
